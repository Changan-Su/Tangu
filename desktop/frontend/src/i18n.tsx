const LS_KEY = 'tangu_locale'

export function resolveInitialLocale(): Locale {
  try {
    const v = localStorage.getItem(LS_KEY)
    if (v === 'en' || v === 'zh') return v
  } catch {
    /* ignore */
  }
  return 'zh' // 默认中文
}

